#!/usr/bin/env python3
# ===============================================================
# Simple script to generate the LUT for the gamma correction
# ===============================================================
import math
import argparse
import datetime
import tomllib

def gen_gamma(size, gamma, maxval):

    arr = [x for x in range(size)]
    arr = [math.pow(x, gamma) for x in arr]
    arr = [x / max(arr) for x in arr]
    return [round(x * maxval) for x in arr]


def gen_file(file, lut, datatype, size, gamma, maxval):
    
    with open(file, "w+") as f:
        f.write("// Gamma correction file, autogenerated by generate_gamma_lut.py script.\n")
        f.write(f"// Generated on {datetime.datetime.now()}\n")
        f.write("// Please do not touch it ! \n\n")

        f.write("#pragma once\n")
        f.write("#ifdef __cplusplus\n")
        f.write("extern \"C\" {\n")
        f.write("#endif\n")
        f.write("#ifdef __cplusplus\n")
        f.write("#include <cstdint>\n")
        f.write("#else\n")
        f.write("#include <stdint.h>\n")
        f.write("#endif\n\n")

        f.write("// PARAMETER USED : \n")
        f.write(f"#define LED_CORR_GAMMA {gamma}\n")
        f.write(f"#define LED_CORR_MAXVAL {maxval}\n")
        f.write(f"#define LED_CORR_SIZE {size}\n")
        f.write(f"// TYPE :  {datatype}\n\n")

        f.write(f"{datatype} gamma_lut[{size}] = {"{"}\n    ")

        newline_index = round(math.sqrt(size))
        valuesize = len(str(size))
        for index, value in enumerate(lut):
            f.write(f"{str(value):>{valuesize}}")

            if index != len(lut) - 1:
                f.write(",")

            if (index % newline_index) == newline_index - 1:
                f.write("\n    ")

        f.write(f"\n{"}"};\n\n")

        f.write("#ifdef __cplusplus\n")
        f.write("}\n")
        f.write("#endif\n\n")

        f.write("// END OF THE FILE\n")

    return

if __name__ == "__main__":

    parser = argparse.ArgumentParser()
    parser.add_argument("--cfgfile", required=True)
    parser.add_argument("--outfile", required=True)
    args = parser.parse_args()

    with open(args.cfgfile, "rb") as f:
        data = tomllib.load(f)["LUT_CFG"]
        gamma = data["gamma"]
        size = data["size"]
        maxval = data["maxval"]
        dtype = data.get("option", "")

    lut = gen_gamma(size, gamma, maxval)

    if (maxval < 256):
        datatype = "uint8_t"
    elif (maxval < 65536):
        datatype = "uint16_t"
    else:
        datatype = "uint32_t"

    datatype = dtype + " " + datatype

    gen_file(args.outfile, lut, datatype, size, gamma, maxval)

